// 練習問題

// using if
if (c == 1) {
  console.log("case 1")
} else if (c==2) {
  console.log("case 2")
} else {
  console.log("default")
  console.log("case 2")
}

// using switch
switch (c) {
  case (c == 1):
    console.log("case 1")
  case (c == 2):
    console.log("case 2")
  default:
    console.log("default")
    console.log("case 2")
}
